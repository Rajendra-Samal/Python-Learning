# Example 2: Tuple with mixed types
person = ("Alice", 25, True)
print("\nPerson tuple:", person)
print("Name:", person[0])
print("Age:", person[1])
print("Type of each element:")
print("  person[0] type:", type(person[0]))
print("  person[1] type:", type(person[1]))
print("  person[2] type:", type(person[2]))
